// Read one ADC channel (on ADC1) and convert raw value to volts (0–3.3V)
static float ADC_ReadVoltage(uint32_t channel)
{
  ADC_ChannelConfTypeDef sConfig = {0};

  sConfig.Channel      = channel;                 // e.g. ADC_CHANNEL_0 (PA0) or ADC_CHANNEL_1 (PA1)
  sConfig.Rank         = 1;                       // single conversion rank
  sConfig.SamplingTime = ADC_SAMPLETIME_56CYCLES; // safe for slow-changing voltages

  if (HAL_ADC_ConfigChannel(&hadc1, &sConfig) != HAL_OK)
  {
    return -1.0f;
  }

  if (HAL_ADC_Start(&hadc1) != HAL_OK)
  {
    return -1.0f;
  }

  if (HAL_ADC_PollForConversion(&hadc1, 10) != HAL_OK)
  {
    HAL_ADC_Stop(&hadc1);
    return -1.0f;
  }

  uint32_t raw = HAL_ADC_GetValue(&hadc1);
  HAL_ADC_Stop(&hadc1);

  // 12-bit ADC: 0–4095 → 0–3.3V
  float voltage = (3.3f * (float)raw) / 4095.0f;
  return voltage;
}

// Select one of the 16 MUX channels using GPIOC pins PC0–PC3 as S0–S3
// channel is 0–15
static void MUX_Select(uint8_t channel)
{
  // S0 = PC0 (bit 0)
  HAL_GPIO_WritePin(GPIOC, GPIO_PIN_0, (channel & 0x01) ? GPIO_PIN_SET : GPIO_PIN_RESET);
  // S1 = PC1 (bit 1)
  HAL_GPIO_WritePin(GPIOC, GPIO_PIN_1, (channel & 0x02) ? GPIO_PIN_SET : GPIO_PIN_RESET);
  // S2 = PC2 (bit 2)
  HAL_GPIO_WritePin(GPIOC, GPIO_PIN_2, (channel & 0x04) ? GPIO_PIN_SET : GPIO_PIN_RESET);
  // S3 = PC3 (bit 3)
  HAL_GPIO_WritePin(GPIOC, GPIO_PIN_3, (channel & 0x08) ? GPIO_PIN_SET : GPIO_PIN_RESET);

  // Small delay to let the mux output settle before ADC reading
  HAL_Delay(2);
}

